package acmicpc;

import java.util.Scanner;

public class M2439_2 {
	public static void main(String[] argv) {
		
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		String string = "";
		
		for (int i = 0; i < n; i++) {
			string += "*";
			System.out.printf("%" + n + "s%n", string);
			
		}
			
		
		
		
	}
}


